export interface EllipticCurveParams extends CurveParams {
  name: string;
  n: bigint;
  h: bigint;
  G: Point;
}

export interface ECKeyPair {
  privateKey: bigint;
  publicKey: Point;
}

export interface ECDSASignature {
  r: bigint;
  s: bigint;
}

